#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	len;

	len = count * size;
	if (!(ptr = (void *)malloc(len)))
		return (0);
	return (ft_memset(ptr, 0, len));
}
